namespace UGESystem
{
    /// <summary>
    /// Defines an enumeration for various player stat types (e.g., Justice, Dominance)
    /// that can be modified as event rewards.
    /// </summary>
    public enum StatType
    {
        /// <summary>No specific stat type.</summary>
        None,
        /// <summary>Represents the 'Justice' stat.</summary>
        Justice,
        /// <summary>Represents the 'Dominance' stat.</summary>
        Dominance,
        /// <summary>Represents the 'Prudence' stat.</summary>
        Prudence
    }
}